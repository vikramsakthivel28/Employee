package condition;

public class DayOf {
	public static void main (String[]args) {
		String day = "tuesday";
		switch (day) {
		case "sunday":System.out.println("Day1");break;
		case "mounday":System.out.println("Day2");break;
		case "tuesday":System.out.println("Day3");break;
		case "wenasday":System.out.println("Day4");break;
		case "thusday":System.out.println("Day5");break;
		case "friday":System.out.println("Day6");break;
		case "sataday":System.out.println("Day7");break;
		 default : System.out.println("Invaild data");
		}
	}

}
